int		ft_atoi(char *str)
{
	int sum;
	int neg;

	sum = 0;
	neg = 1;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
			neg = -1;
	}
	while (*str >= '0' && *str <= '9')
		sum = sum * 10 + (*str++ - '0');
	return (sum * neg);
}
